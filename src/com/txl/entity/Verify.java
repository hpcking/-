package com.txl.entity;

public class Verify {
private int id;
private int classid;
private int userid;
private String word;
private User user;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getClassid() {
	return classid;
}
public void setClassid(int classid) {
	this.classid = classid;
}
public int getUserid() {
	return userid;
}
public void setUserid(int userid) {
	this.userid = userid;
}
public String getWord() {
	return word;
}
public void setWord(String word) {
	this.word = word;
}
public User getUser() {
	return user;
}
public void setUser(User user) {
	this.user = user;
}
}
